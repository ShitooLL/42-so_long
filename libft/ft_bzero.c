#include "libft.h"

void	ft_bzero(void *ptr, size_t n)
{
	size_t	i;
	char	*tmpptr;

	tmpptr = ptr;
	i = 0;
	while (i < n)
	{
		*(tmpptr + i) = '\0';
		i++;
	}
}
